function solution(nums) {
  let sorted = [];
  nums !== null ? (sorted = nums.sort((a, b) => a - b)) : sorted;
  return sorted;
}
solution([1, 2, 10, 50, 5]); // 1, 2, 5, 10, 50
solution(null); // []
